    private void okBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okBtnActionPerformed
        List<Organizador> organizadoresSeleccionados = new ArrayList<>();

        DefaultTableModel tableModel = (DefaultTableModel) this.organizadoresTable.getModel();

        for (int i = 0; i < tableModel.getRowCount(); i++) {

            Boolean isSelected = (Boolean) tableModel.getValueAt(i, 0);
            if (isSelected) {
                Organizador org = new Organizador();
                org.setCip((int) tableModel.getValueAt(i, 1));
                org.setDni((String) tableModel.getValueAt(i, 2));
                org.setOrganizador((String) tableModel.getValueAt(i, 3));
                org.setCelular((String) tableModel.getValueAt(i, 4));
                org.setCorreo((String) tableModel.getValueAt(i, 5));

                organizadoresSeleccionados.add(org);
            }
        }

        this.parentFrame.llenarTablaOrganizadores(organizadoresSeleccionados);

        this.setVisible(false);
        this.dispose();
    }//GEN-LAST:event_okBtnActionPerformed


    public void llenarTabla() {

        DefaultTableModel tableModel = (DefaultTableModel) this.organizadoresTable.getModel();
        tableModel.setRowCount(0); //Limpiar tabla
        Object cells[] = new Object[6];

        for (Organizador org : organizadores) {
            cells[0] = false;
            cells[1] = org.getCip();
            cells[2] = org.getDni();
            cells[3] = org.getOrganizador();
            cells[4] = org.getCelular();
            cells[5] = org.getCorreo();

            tableModel.addRow(cells);
        }
    }

